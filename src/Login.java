    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        DefaultTableModel dm3=(DefaultTableModel)jTable3.getModel();
        while(dm3.getRowCount()>0)
            dm3.removeRow(0);
        
         try
        {
            MongoClient mongoClient = new MongoClient("localhost", 27017);
//MongoClient mongoClient = new MongoClient( "localhost" , 27017 );
        DB db = mongoClient.getDB( "tb" );
      //  System.out.println("Connected.!");
        
        DBCollection coll = db.getCollection("det");
        
        String day=(String)jList1.getSelectedValue();
        
        BasicDBObject find=new BasicDBObject("Day",day);
        DBCursor docs=coll.find(find);
        while(docs.hasNext())
        {
            DBObject doc=docs.next();
            dm3.addRow(new Object[]{doc.get("Mor"),doc.get("Aft"),doc.get("Eve")});
            
        }
        
        int sum=0;
        for(int k=0;k<=2;k++)
            
        {
            Object seat=dm3.getValueAt(0, k);
            String seats=(String)seat;
            int sea=Integer.valueOf(seats);
           sum=sum+sea;
        }
        int avg=sum/3;
        
        
        jProgressBar1.setValue(sum);
        jProgressBar1.setString(""+sum+" out of 600 seats");
        jProgressBar2.setValue(avg/2);
        jProgressBar2.setString(""+(avg/2)+" %");
        jLabel21.setText(""+sum+" Seats");
        jLabel22.setText(""+(avg/2)+" %");
        
        
        }
         
         catch(Exception e)
         {
             
         }
    }//GEN-LAST:event_jList1ValueChanged

